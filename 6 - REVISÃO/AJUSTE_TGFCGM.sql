-- AJUSTE ABA GERAL VENDAS/COMPRAS DAS PREFERENCIAS DO GOL.
UPDATE TSIPAR 
   SET INTEIRO = 0   
 WHERE (CHAVE = 'GOL-QTDEPARC' OR
        CHAVE = 'GOL-QTDEPROD' OR 
        CHAVE = 'GOL-QTDEMARC' OR 
        CHAVE = 'GOL-QTDEGRUPOS' OR 
        CHAVE = 'GOL-QTDEVENDS' OR 
        CHAVE = 'GOL-QTDEPERFIS')
   AND CODUSU = 0; COMMIT;

-- AJUSTE ABA GERAL CICLO OPERACIONAL FINANCEIRO DAS PREFERENCIAS DO GOL.   
