-- COPIANDO GASTOS VARIAVEIS ATRIBUIDOS NA EMPRESA 1 PARA AS DEMAIS EMPRESAS.
UPDATE TGFCGM D
   SET (D.PERCCFFAB,
        D.PERCCFOUTROS,
        D.PERCCFSERVICO,
        D.PERCCOFINS,
        D.PERCCSL,
        D.PERCCUSVAR,
        D.PERCCVARSEMST,
        D.PERCMARGEM,
        D.PERCPIS,
        D.PERCIRPJ,
        D.PERCCPP) =
       (SELECT O.PERCCFFAB,
               O.PERCCFOUTROS,
               O.PERCCFSERVICO,
               O.PERCCOFINS,
               O.PERCCSL,
               O.PERCCUSVAR,
               O.PERCCVARSEMST,
               O.PERCMARGEM,
               O.PERCPIS,
               O.PERCIRPJ,
               O.PERCCPP
          FROM TGFCGM O
         WHERE O.CODEMP = 1
           AND O.ANO    = 1900
           AND O.MES    = 1)
 WHERE D.CODEMP <> 1; COMMIT;

